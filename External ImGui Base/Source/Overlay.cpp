// Main rendering function (includes menu)
void render()
{
	ImGui_ImplDX9_NewFrame();
	ImGui_ImplWin32_NewFrame();
	ImGui::NewFrame();

	/* Menu */
	if (ShowMenu)
	{
		ImGui::Begin("My ImGui Menu", &ShowMenu, ImVec2(430, 400), 1.0f, ImGuiWindowFlags_NoResize | ImGuiWindowFlags_NoCollapse);

		if (ImGui::Button("Home", ImVec2(125.0f, 0.0f)))
			TabPos = 0;
		ImGui::SameLine();
		if (ImGui::Button("Tab 1", ImVec2(125.0f, 0.0f)))
			TabPos = 1;
		ImGui::SameLine();
		if (ImGui::Button("Tab 2", ImVec2(125.0f, 0.0f)))
			TabPos = 2;

		switch (TabPos) {
		case 0:																									// Home
			ImGui::Separator();
			ImGui::Text("Welcome to your menu\n");

			ImGui::Separator();
			ImGui::Text("Credits:");
			ImGui::BulletText("43apple          - being cool and epic");
			break;
		case 1:																									// Tab 1
			ImGui::Separator();
			ImGui::Checkbox("This is checkbox is toggled using LCONTROL", &CheckBox1);
			ImGui::Checkbox("This is a default checkbox", &CheckBox2);
			break;
		case 2:																									// Tab 2
			ImGui::Separator();
			ImGui::Text("I am a third tab");
			break;
		}
		ImGui::End();
	}

	// Draw FPS
	if (DrawFps)
	{
		char dist[64];
		sprintf_s(dist, "%.1f FPS\n", ImGui::GetIO().Framerate);
		DrawText1(15, 27, dist, &Colors.Red);
	}

	// Draw watermark
	if (DrawWatermark)
	{
		DrawText1(15, 11, "External ImGui Overlay by 43apple", &Colors.Red);
	}

	ImGui::EndFrame();
	D3dDevice->SetRenderState(D3DRS_ZENABLE, false);
	D3dDevice->SetRenderState(D3DRS_ALPHABLENDENABLE, false);
	D3dDevice->SetRenderState(D3DRS_SCISSORTESTENABLE, false);
	D3dDevice->Clear(0, NULL, D3DCLEAR_TARGET, D3DCOLOR_ARGB(0, 0, 0, 0), 1.0f, 0);

	if (D3dDevice->BeginScene() >= 0)
	{
		ImGui::Render();
		ImGui_ImplDX9_RenderDrawData(ImGui::GetDrawData());
		D3dDevice->EndScene();
	}
	HRESULT result = D3dDevice->Present(NULL, NULL, NULL, NULL);

	if (result == D3DERR_DEVICELOST && D3dDevice->TestCooperativeLevel() == D3DERR_DEVICENOTRESET)
	{
		ImGui_ImplDX9_InvalidateDeviceObjects();
		D3dDevice->Reset(&d3dpp);
		ImGui_ImplDX9_CreateDeviceObjects();
	}
}

// Creating the overlay window to draw ImGui onto
void xCreateWindow()
{
	CreateThread(0, 0, (LPTHREAD_START_ROUTINE)SetWindowToTarget, 0, 0, 0);

	WNDCLASSEX wc;
	ZeroMemory(&wc, sizeof(wc));
	wc.cbSize = sizeof(wc);
	wc.lpszClassName = "fgers";
	wc.lpfnWndProc = WinProc;
	RegisterClassEx(&wc);

	if (hWnd)
	{
		GetClientRect(hWnd, &GameRect);
		POINT xy;
		ClientToScreen(hWnd, &xy);
		GameRect.left = xy.x;
		GameRect.top = xy.y;

		Width = GameRect.right;
		Height = GameRect.bottom;
	}
	else
		exit(2);

	Window = CreateWindowEx(NULL, "fgers", "fgers1", WS_POPUP | WS_VISIBLE, 0, 0, Width, Height, 0, 0, 0, 0);

	DwmExtendFrameIntoClientArea(Window, &Margin);
	SetWindowLong(Window, GWL_EXSTYLE, WS_EX_TRANSPARENT | WS_EX_TOOLWINDOW | WS_EX_LAYERED);
	ShowWindow(Window, SW_SHOW);
	UpdateWindow(Window);
}

